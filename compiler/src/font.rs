//! Built-in 8×8 pixel font for the `print()` builtin.
//! Covers printable ASCII 32–127 (96 tiles × 16 bytes = 1536 bytes).
//! Each tile is 8×8 pixels in Game Boy 2bpp format.
//! Font design: minimal 1-pixel-wide strokes, color 3 (black) on 0 (white).

/// Generate 2bpp tile data for a single character from an 8-line pixel map.
/// Each line is a &str of 8 chars: '.' = 0, '#' = 3 (dark).
fn char_tile(rows: [&str; 8]) -> [u8; 16] {
    let mut tile = [0u8; 16];
    for (r, row) in rows.iter().enumerate() {
        let mut lo: u8 = 0;
        let mut hi: u8 = 0;
        for (c, ch) in row.chars().enumerate() {
            let bit = 7 - c;
            let val: u8 = match ch {
                '#' => 3,
                '1' => 1,
                '2' => 2,
                _ => 0, // '.'
            };
            if val & 1 != 0 {
                lo |= 1 << bit;
            }
            if val & 2 != 0 {
                hi |= 1 << bit;
            }
        }
        tile[r * 2] = lo;
        tile[r * 2 + 1] = hi;
    }
    tile
}

